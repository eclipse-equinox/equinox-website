<li>Dove right into the P2 discussion.. Quick overview of needs:</li>
<ul>
<li>Verify signatures on content as it is downloaded</li>
<li>UI for signature information about signed content</li>
<li>UI for certificate details attached to a signature</li>
<li>Prompt for trust when downloading signed content</li>
<li>Secure connections to the repositories</li>
<li>Prompt for passwords, etc when connecting to repositories</li>
