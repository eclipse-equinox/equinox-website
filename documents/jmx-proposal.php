<p class=bar>Remote management using JMX</p>
<p>The goal of Eclipse to become a platform to produce flexible and dynamic applications,
while realized, does not currently allow for application
management from a remote location.  This incubator carries the very modest scope
  of providing an extensible framework in support of running a JMX
  server in an instance of OSGi. The framework then allows users to contribute
  providers
which in turn expose managed objects (i.e., MBeans). 
<p>As part of this work we intend
    to implement a rudimentary Eclipse client user interface to demonstrate interaction
  with local or remote MBeans.
